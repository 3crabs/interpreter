void main() {
    int a = 1;
    int b = 2;
    int c = 3;
    print(a + b + c);
    print(a - b - c);
    print(a * b * c);
    print(a / b / c);
    print(a % b % c);
    print(a >> b >> c);
    print(a << b << c);
    print(a == b == c);
    print(a != b != c);
    print(a < b < c);
    print(a > b > c);
    print(a <= b <= c);
    print(a >= b >= c);
    print(--a);
    print(++b);
    print(+-c);
    print(-+c);
}
